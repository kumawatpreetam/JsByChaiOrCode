const month = 3;

switch (month) {
  case 1:
    console.log("This is January Month");

    break;
  case 2:
    console.log("This is Febraury Month");

    break;

  case 3:
    console.log("This is March Month");

    break;

  case 4:
    console.log("This is April Month");

    break;

  default:
    console.log("Enter Valid Month number");

    break;
}
